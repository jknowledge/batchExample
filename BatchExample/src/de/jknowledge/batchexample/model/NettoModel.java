package de.jknowledge.batchexample.model;

public class NettoModel {
	
	private String name;
	
	private Float price;
	
	private Float tax;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Float getPrice() {
		return price;
	}

	public void setPrice(Float price) {
		this.price = price;
	}

	public Float getTax() {
		return tax;
	}

	public void setTax(Float tax) {
		this.tax = tax;
	}
	
	

}
